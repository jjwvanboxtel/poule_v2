// 7. Token replacement — no unreplaced placeholder tokens
// ===========================================================================

/**
 * Verify that every placeholder token is removed from the rendered output.
 *
 * An unreplaced token (e.g., the literal string '{LANG_LOGIN}') in the
 * final HTML indicates a missing substitution and would be visible to
 * end-users as broken markup.
 */
class TestOfFormTokenReplacement extends UiMarkupAssertions
{
    function testNoUnreplacedTokensInLoginForm()
    {
        $html   = renderLoginForm();
        $tokens = array_keys(loginFormDefaults());

        foreach ($tokens as $token) {
            $this->assertHtmlNotContains(
                $html,
                $token,
                "Placeholder {$token} was not replaced in the login form."
            );
        }
    }

    function testNoUnreplacedTokensInCompetitionAddForm()
    {
        $html   = renderCompetitionAddForm();
        $tokens = array_keys(addFormDefaults());

        foreach ($tokens as $token) {
            $this->assertHtmlNotContains(
                $html,
                $token,
                "Placeholder {$token} was not replaced in the competition add form."
            );
        }
    }

    function testNoUnreplacedTokensInUserAddForm()
    {
        $html   = renderUserAddForm();
        $tokens = array_keys(userAddFormDefaults());

        foreach ($tokens as $token) {
            $this->assertHtmlNotContains(
                $html,
                $token,
                "Placeholder {$token} was not replaced in the user add form."
            );
        }
    }
}

