// 4. Form field rows — {CONTENT} insertion and label visibility
// ===========================================================================

/**
 * Verify that field rows inserted through the {CONTENT} placeholder render
 * inside the form table and preserve the label/input pairing that the
 * module classes generate.
 *
 * These tests use controlled HTML snippets that mirror the row format
 * produced by competitions.class.php, users.class.php, etc.
 */
class TestOfFormFieldRows extends UiMarkupAssertions
{
    function testSingleTextFieldRowRendersInsideForm()
    {
        $fieldRow = '<tr><td>Naam:</td>'
                  . '<td><input type="text" name="competitionname" value="" /></td>'
                  . '</tr>';
        $html = renderCompetitionAddForm(array('{CONTENT}' => $fieldRow));

        $this->assertFormField($html, 'competitionname');
    }

    function testFieldLabelAppearsAlongsideInput()
    {
        $fieldRow = '<tr><td>Omschrijving:</td>'
                  . '<td><input type="text" name="description" value="" /></td>'
                  . '</tr>';
        $html = renderCompetitionAddForm(array('{CONTENT}' => $fieldRow));

        $this->assertHtmlContains(
            $html,
            'Omschrijving:',
            'Expected the field label to appear in the rendered form output.'
        );
        $this->assertFormField($html, 'description');
    }

    function testMultipleFieldRowsAllRenderCorrectly()
    {
        $content = '<tr><td>Voornaam:</td>'
                 . '<td><input type="text" name="firstname" value="" /></td></tr>'
                 . '<tr><td>Achternaam:</td>'
                 . '<td><input type="text" name="lastname" value="" /></td></tr>'
                 . '<tr><td>E-mail:</td>'
                 . '<td><input type="text" name="emailaddress" value="" /></td></tr>';
        $html = renderUserAddForm(array('{CONTENT}' => $content));

        $this->assertFormField($html, 'firstname');
        $this->assertFormField($html, 'lastname');
        $this->assertFormField($html, 'emailaddress');
    }

    function testSelectFieldRowRendersInsideForm()
    {
        $fieldRow = '<tr><td>Status:</td>'
                  . '<td><select name="status">'
                  . '<option value="1">Actief</option>'
                  . '<option value="0">Inactief</option>'
                  . '</select></td></tr>';
        $html = renderCompetitionAddForm(array('{CONTENT}' => $fieldRow));

        $this->assertFormField($html, 'status');
    }

    function testHiddenFieldIsPreservedInOutput()
    {
        // Hidden fields in {CONTENT} must survive template substitution
        // intact because they carry business-logic values.
        $fieldRow = '<tr><td colspan="2">'
                  . '<input type="hidden" name="competition_id" value="42" />'
                  . '</td></tr>';
        $html = renderCompetitionAddForm(array('{CONTENT}' => $fieldRow));

        $this->assertHiddenField($html, 'competition_id');
    }
}

