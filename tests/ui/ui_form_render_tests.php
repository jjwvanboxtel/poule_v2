/**
 * Return the default placeholder values used by renderLoginForm().
 *
 * @return array<string,string>  Map of placeholder token → default value.
 */
function loginFormDefaults()
{
    return array(
        '{LANG_LOGIN}'    => 'Inloggen',
        '{LOGIN_ACTION}'  => '/?com=2&option=login',
        '{LOGIN_MSG_WRAPPER}' => '',
        '{LANG_EMAIL}'    => 'E-mail',
        '{LANG_PASSWORD}' => 'Wachtwoord',
        '{NEW_CUSTOMER}'  => '<a href="/?com=2&amp;option=new">Nieuw account</a>',
        '{LOGIN_LOST}'    => '<a href="/?com=2&amp;option=lost">Wachtwoord vergeten</a>',
        '{CSRF_TOKEN}'    => '<input type="hidden" name="csrf_token" value="test_token_abc123" />',
    );
}

/**
 * Render the login form template with the supplied placeholder replacements.
 *
 * Loads modules/usercontrol/loginscreen.tpl.php, strips the PHP die()
 * guard, and substitutes every placeholder token. Any token not supplied
 * in $replacements receives a sensible default.
 *
 * @param  array $replacements  Map of placeholder token → replacement string.
 * @return string               The fully rendered HTML string.
 */
function renderLoginForm($replacements = array())
{
    $values  = array_merge(loginFormDefaults(), $replacements);
    $tplPath = dirname(__FILE__)
             . '/../../modules/usercontrol/loginscreen.tpl.php';
    $tpl     = file_get_contents($tplPath);

    // Strip the PHP die() guard that prevents direct file access.
    $tpl = preg_replace('/^<\?php die\(\); \?>\s*/s', '', $tpl);

    return str_replace(array_keys($values), array_values($values), $tpl);
}

